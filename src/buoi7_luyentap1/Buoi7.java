    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("a.txt"));
            oos.writeObject(lisHocsinh);
            oos.flush();
            oos.flush();
            oos.close();
            oos.close();
            JOptionPane.showMessageDialog(this, "Save thanh cong");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi save ");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            if (txtHoTen.getText().equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(this, "ho ten khong duoc rong");
                txtHoTen.requestFocus();
                return;
            }
            if (txtTuoi.getText().equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(this, "tuoi khong duoc rong");
                txtTuoi.requestFocus();
                return;
            }
            int tuoi;
            try {
                tuoi = Integer.parseInt(txtTuoi.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "tuoi phai la so");
                txtTuoi.requestFocus();
                return;
            }
            if (tuoi < 15 || tuoi > 65) {
                JOptionPane.showMessageDialog(this, "tuoi phai 15-65");
                txtTuoi.requestFocus();
                return;
            }
            String name = txtHoTen.getText();
            String ngoaiNgu = (String) cboNgoaingu.getSelectedItem();
            Hocsinh hs = new Hocsinh(name, ngoaiNgu, tuoi);
            lisHocsinh.add(hs);
            model.addRow(new Object[]{hs.getHoten(), hs.getTuoi(), hs.getNgoaiNgu(), hs.getStatus(tuoi)});
            if (lisHocsinh.size() > 0) {
                index = 0;
                showDetail();
            } else {
                clearForm();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Loi add");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            if (lisHocsinh.size() == 0) {
                JOptionPane.showMessageDialog(this, "Không có dữ liệu để xóa");
                return;
            }
            int hoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không");
            if (hoi != JOptionPane.YES_OPTION) {
                return;
            }
            lisHocsinh.remove(index);
            model.removeRow(index);
            //
            if (lisHocsinh.size() > 0) {
                index = 0;
                showDetail();
            } else {
                clearForm();
            }
            JOptionPane.showMessageDialog(this, "Xóa dữ liệu thành công");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi nốt delete");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            if (txtHoTen.getText().equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(this, "ho ten khong duoc rong");
                txtHoTen.requestFocus();
                return;
            }
            if (txtTuoi.getText().equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(this, "tuoi khong duoc rong");
                txtTuoi.requestFocus();
                return;
            }
            int tuoi;
            try {
                tuoi = Integer.parseInt(txtTuoi.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "tuoi phai la so");
                txtTuoi.requestFocus();
                return;
            }
            if (tuoi < 15 || tuoi > 65) {
                JOptionPane.showMessageDialog(this, "tuoi phai 15-65");
                txtTuoi.requestFocus();
                return;
            }
            String name = txtHoTen.getText();
            String ngoaiNgu = (String) cboNgoaingu.getSelectedItem();
            Hocsinh hs = new Hocsinh(name, ngoaiNgu, tuoi);
            index = tblTable.getSelectedRow();
            lisHocsinh.set(index, hs);
            model.setValueAt(name, index, 0);
            model.setValueAt(tuoi, index, 1);
            model.setValueAt(ngoaiNgu, index, 2);
            

            if (lisHocsinh.size() > 0) {
                index = 0;
                showDetail();
            } else {
                clearForm();
            }
            JOptionPane.showMessageDialog(this, "Update thanh cong");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Loi update");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

