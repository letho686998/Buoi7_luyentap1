    private void fillToTable() {
        model.setRowCount(0);
        for (Hocsinh hs : lisHocsinh) {
            model.addRow(new Object[]{hs.getHoten(), hs.getTuoi(), hs.getNgoaiNgu(), hs.getStatus(hs.getTuoi())});
        }
    }

    private void showDetail() {
        Hocsinh hs = lisHocsinh.get(index);
        txtHoTen.setText(hs.getHoten());
        txtTuoi.setText(hs.getTuoi() + "");
        cboNgoaingu.setSelectedItem(hs.getNgoaiNgu());
        tblTable.setRowSelectionInterval(index, index);

    }

    private void clearForm() {
        txtHoTen.setText("");
        txtTuoi.setText("");
        cboNgoaingu.setSelectedItem("");
        index = -1;
    }
